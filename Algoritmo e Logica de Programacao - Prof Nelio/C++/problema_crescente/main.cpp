#include <iostream>

using namespace std;

int main()
{
    int x, y;

    while (x != y) {
         cout << "Digite dois numeros: " << endl;
         cin >> x;
         cin >> y;

         if (x > y) {
            cout << "Decrescente" << endl;
            }
            else if (x < y){
                cout << "Crescente" << endl;
            }
    }















}
